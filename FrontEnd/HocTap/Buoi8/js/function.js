
// Hàm tính tổng
const congSum = (a, b) => {
    return a + b;
}

// Hàm tính hiệu
const truSum = (a, b) => {
    return a - b;
}

// Hàm tính tích
const nhanSum = (a, b) => {
    return a * b;
}

// Hàm tính thương
const chiaSum = (a, b) => {
    return a / b;
}

console.log("Hàm tính tổng: " + congSum(1, 2));
console.log("Hàm tính hiệu: " + truSum(1, 2));
console.log("Hàm tính tích: " + nhanSum(1, 2));
// console.log("Hàm tính thương: " + chiaSum(1, 2));

var so1 = 18;
var so2 = 6;

console.log("Hàm tính thương: " + chiaSum(so1, so2));