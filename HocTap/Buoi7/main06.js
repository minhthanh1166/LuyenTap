// Cac toán tử số học: * / + =, % , ++, --
let a = 123;
let b = 112;

kq = a * b;
console.log(a, " * ", b, " = ", kq);
kq = a / b;
console.log(a, " / ", b, " = ", kq);
kq = a + b;
console.log(a, " + ", b, " = ", kq);
kq = a - b;
console.log(a, " - ", b, " = ", kq);
kq = a++;
console.log("a++ ", kq);
kq = ++a;
console.log("++a ", kq);
